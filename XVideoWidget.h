protected:
	//重载3个函数

	//刷新显示
	void paintGL();

	//初始化gl
	void initializeGL();

	//窗口尺寸变化
	void resizeGL(int width, int height);

private:
	std::mutex mux;

	//shader程序
	QGLShaderProgram program;

	//从shader获取的yuv变量地址
	GLuint unis[3] = { 0 };

	//opengl的texture地址
	GLuint texs[3] = { 0 };

	//材质内存空间
	unsigned char *datas[3] = { 0 };

	//设置宽度和高度
	int width = 240;
